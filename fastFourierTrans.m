function [X,f] = fastFourierTrans(x,fs)
%保留相位的快速傅里叶变换函数，已处理好不需要的值。

%注意：针对时域输入为实数的信号，DFT过后的振幅M_r = A_0*N/2,其中A_0是时域信号中某个频率信号的振幅，N是采样点数。
%针对时域输入为复数的信号，DFT过后的振幅M_c = A_0*N
%0Hz的直流分量为X(0)=D_0*N

N = length(x);%采样点数
X = fft(x)/N;%FFT变换并除以采样点个数获得正确的变换后的值
X = X(1:N/2+1);%取前N/2+1个点为有效值
X(2:end) = X(2:end)*2;%除开0Hz这个特殊点，其余值还需要乘以2
f = (0:N/2)*fs/N;%计算0~N/2这么多点的各点实际频率